     // Método para generar pedidos con un hilo
    private void generarPedidosConHilo(int nPedido) {
        List<Pedido> listaPedidos = new ArrayList<>();
        Random random = new Random();

        for (int i = 0; i < nPedido; i++) {
            Pedido pedido = new Pedido();

            int numItems = 5 + random.nextInt(6); // Genera entre 5 y 10 ítems por pedido
            for (int j = 0; j < numItems; j++) {
                int item = 1 + random.nextInt(60); // Ítems entre 1 y 60
                pedido.agregarItem(item);
            }

            listaPedidos.add(pedido);
        }

        pilaPedidos.addAll(listaPedidos); // Añadir los pedidos generados a la cola FIFO

        // Enviar los pedidos generados al almacén
        for (Pedido pedido : listaPedidos) {
            almacen.agregarPedido(pedido);
        }

        // Imprimir los pedidos generados en la consola
        for (Pedido pedido : listaPedidos) {
            System.out.println("Pedido:");
            for (Integer item : pedido.getItems()) {
                System.out.println("Item " + item);
            }
            System.out.println();  // Espacio entre pedidos
        }
    }

    // Método para pintar los paneles correspondientes al primer pedido en la cola
    private void pintarItemsDelPedido() {
        detenerPintado = false;

        if (!pilaPedidos.isEmpty()) {
            Pedido primerPedido = pilaPedidos.poll();

            for (Integer item : primerPedido.getItems()) {
                if (detenerPintado) {
                    break;
                }
                pintarPanel(item);
                try {
                    Thread.sleep(500);
                } catch (InterruptedException e) {
                    Thread.currentThread().interrupt();
                }
            }
        }
    }

    // Método para pintar el panel correspondiente a un ítem
    private void pintarPanel(int item) {
        Panel panel = obtenerPanelPorNombre("item" + item);
        if (panel != null) {
            panel.setBackground(Color.BLUE);
        } else {
            System.out.println("Panel no encontrado: item" + item);
        }
    }

    // Método para obtener el panel por su nombre dentro de jPanel2
    private Panel obtenerPanelPorNombre(String nombrePanel) {
        for (Component comp : jPanel2.getComponents()) {
            if (comp instanceof Panel && comp.getName() != null && comp.getName().equals(nombrePanel)) {
                return (Panel) comp;
            }
        }
        return null;
    }

