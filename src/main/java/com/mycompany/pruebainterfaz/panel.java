    private void btnGenerarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarPedidoActionPerformed
        // TODO add your handling code here:
        try {
            int nPedido = Integer.parseInt(jtfNumeroPedidos.getText().trim());

            if (nPedido >= 5 && nPedido <= 10) {
                String resultado = "Generando " + nPedido + " pedidos.";
                JOptionPane.showMessageDialog(this, resultado);

                // Crear y ejecutar un hilo para la generación de pedidos
                Thread hiloGenerarPedidos = new Thread(new Runnable() {
                    @Override
                    public void run() {
                        generarPedidosConHilo(nPedido);
                    }
                });
                hiloGenerarPedidos.start();

            } else {
                JOptionPane.showMessageDialog(this, "El número de pedidos debe estar entre 5 y 10.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingresa un número válido.");
        }

    }//GEN-LAST:event_btnGenerarPedidoActionPerformed

